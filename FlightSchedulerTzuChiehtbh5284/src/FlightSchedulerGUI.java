    private void cancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelActionPerformed
        // TODO add your handling code here:
        String customer =(String) cancelCustomerComboBox.getSelectedItem();
        Date dates =(Date) cancelDateComboBox.getSelectedItem();
        java.util.Date utilStartDate = dates;
        java.sql.Date sqlStartDate = new java.sql.Date(utilStartDate.getTime());
        
        Cancel.setBookingFlight(sqlStartDate,customer);    
       
       
        if(Cancel.checkBooking()==false){
        addCancelBox.setText("Sorry, the customer does not book flights");
            }
        else{
        
        Cancel.cancelBooking(sqlStartDate, customer);
        Cancel.setNextBooker(Cancel.getBookingFlight(), sqlStartDate);
        
        if(Cancel.checkWaiter() == true){
       
        Cancel.addBooker(Cancel.getNextBooker() , Cancel.getBookingFlight() , sqlStartDate, Cancel.getNextTimestamp());
        Cancel.cancelWaiter(sqlStartDate, Cancel.getBookingFlight(), Cancel.getNextBooker() );
           
        addCancelBox.setText("Booker:  "+ customer + "  is cancelled. Waiter:  "+ Cancel.getNextBooker() +"  is added into booking list");
        Cancel.setNextBooker(Cancel.getBookingFlight(), sqlStartDate);
       
        }
        else{
         addCancelBox.setText("Booker:  "+ customer + "  is cancelled.");
                }
        }
        
        
            
        
    }//GEN-LAST:event_cancelActionPerformed

