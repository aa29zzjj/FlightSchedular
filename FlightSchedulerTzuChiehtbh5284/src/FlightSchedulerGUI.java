    private void dropActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropActionPerformed
        // TODO add your handling code here:
        String flight =(String) dropFlightComboBox.getSelectedItem();
        Drop.dropFlight(flight);
        bookingFlightComboBox.setModel(new javax.swing.DefaultComboBoxModel(Flight.getAllFlights().toArray()));
        bookingFlightComboBox2.setModel(new javax.swing.DefaultComboBoxModel(Flight.getAllFlights().toArray()));
        bookingFlightComboBox3.setModel(new javax.swing.DefaultComboBoxModel(Flight.getAllFlights().toArray()));
        dropFlightComboBox.setModel(new javax.swing.DefaultComboBoxModel(Flight.getAllFlights().toArray()));
        
        
        addDropResult.setText("");
        Drop.setCustomerDate(flight);
                  
            if(Drop.checkBookingCustomers()==false){
            addDropResult.append("There is no customer in this flight!");
            }
            else{     
            for(Date b: Day.getAllDay()){
                Drop.setCustomerDate(flight);
            for(String a: Flight.getAllFlights()){                
            if(Drop.checkFlightSeats(Drop.getDate(), a) == false){                          
            }
            else{ 
              if(Drop.checkBookingCustomers()==true){
                 while(Drop.checkCustomerFlight()==true){
                     
                Drop.addBookings(Drop.getCustomer(), Drop.getEmptyFlight(), Drop.getDate(), Drop.getTimestamp());
                addDropResult.append("Customer: "+Drop.getCustomer()+" is changed to the flight: "+ Drop.getEmptyFlight() +" from the flight: "+ flight +" in date: "+ Drop.getDate()+"\n");
                Drop.cancelBooking(Drop.getCustomer() , flight, Drop.getDate());              
                Drop.setCustomerDate(flight);  
                Drop.checkFlightSeats(Drop.getDate(), a);
                Drop.checkBookingCustomers();
                 }
              }
              else{}
               }          
                
                }
            if(Drop.checkBookingCustomers()==true){
                Drop.cancelBooking(Drop.getCustomer() , flight, b);      
                addDropResult.append("Customer: "+Drop.getCustomer()+" is cancelled from the flight: "+ flight +"\n");
            }else{
            }
            }
          }
         for(String a: Drop.getAllFlightWaiter(flight)){
          addDropResult.append("Waiters: "+ a +"  is cancelled from the flight: "+ flight +"\n");
         }         
         Drop.dropWaitListFlight(flight);
    }//GEN-LAST:event_dropActionPerformed

